    static function getDatabase($user = '', $password = '', $db = '', $address = '')
    {
        if (!self::$db) {
            self::$db = new Database($user, $password, $db, $address);
        }
        return self::$db;
    }

    static function getAuth($session = null)
    {
        if (!self::$auth) {
            self::$auth = new Auth($session, self::getDatabase(DATABASE_USER, DATABASE_PASSWORD, DATABASE_NAME, DATABASE_HOST));
        }
        return self::$auth;
    }

