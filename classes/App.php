    static function decrypt($password, $hash)
    {
        return self::encrypt($password) == $hash;
    }

    // Encrypt cookie
    static function encryptCookie($value)
    {
        return base64_encode(mcrypt_encrypt(MCRYPT_RIJNDAEL_256, md5(CRYPT_KEY_SALT), $value, MCRYPT_MODE_CBC, md5(md5(CRYPT_KEY_SALT))));
    }

    // Decrypt cookie
    static function decryptCookie($value)
    {
        return rtrim(mcrypt_decrypt(MCRYPT_RIJNDAEL_256, md5(CRYPT_KEY_SALT), base64_decode($value), MCRYPT_MODE_CBC, md5(md5(CRYPT_KEY_SALT))), "\0");
    }

