    static function contains($str, array $arr)
    {
        foreach ($arr as $a) {
            if (stripos($a, $str) !== false) return true;
        }
        return false;
    }

    static function flashes($session, $theme)
    {
        if ($session->hasFlashes()) {
            if ($theme != 'admin') {
                foreach ($session->getFlashes() as $key => $value) {
                    echo '
               <div class="alert alert-' . $value['type'] . ' alert-dismissible text-center" role="alert">
               <button type="button" class="close" data-dismiss="alert" aria-label="Close">
               <span aria-hidden="true">&times;
               </span>
               </button>' . $value['msg'] . '
               </div>';
                }
            } else {
                foreach ($session->getFlashes() as $key => $value) {
                    echo '
               <script type="text/javascript">
          				$.notify({
          					icon:"add_alert",
          					message:"' . $value['msg'] . '"
          				},
          				{
          					type:"' . $value['type'] . '",
          					timer:3,
          					placement:{from:"top",align:"right"}
          				})
          			</script>';
                }
            }
        }
    }

    static function encrypt($password)
